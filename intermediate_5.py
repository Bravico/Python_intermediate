# 转义
# ------------------Python转义--------------------------------
print("I'm OK!")  # 使用单引号，双引号进行内部引号不转义
print('I\'m OK!')  # 使用\对单引号进行转义
print('\\\n\\')  # 对\进行转义

print('\\\t\\')  # 同上
print(r'\\\t\\')  # 默认\t中不转义

